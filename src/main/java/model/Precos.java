package model;

public class Precos {

    private int idPreco;
    private String permanenciaPreco;
    private double valor;

    public int getIdPreco() {
        return idPreco;
    }

    public void setIdPreco(int idPreco) {
        this.idPreco = idPreco;
    }

    public String getPermanenciaPreco() {
        return permanenciaPreco;
    }

    public void setPermanenciaPreco(String permanenciaPreco) {
        this.permanenciaPreco = permanenciaPreco;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }
}
